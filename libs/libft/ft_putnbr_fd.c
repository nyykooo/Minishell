#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	answ;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else if (n <= 9)
	{
		answ = (n + 48);
		ft_putchar_fd(answ, fd);
	}
}
